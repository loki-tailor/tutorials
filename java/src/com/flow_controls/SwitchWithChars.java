public class SwitchWithChars {

	public static void main(String[] args) {
		char var1 = '7';
		char var2 = 7;
		Integer var3 = 7;
		Character var4 = '7';
		Character var5 = 7;

		switch (var5) {
		case 7:
			System.out.println("Lucky no 7");
			break;
		default:
			System.out.println("DEFAULT");
		}

	}

}
