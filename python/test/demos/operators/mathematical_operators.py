a, b = 10, 7
print("a + b\t:", a + b)
print('a - b\t:', a - b)
print('a * b\t:', a * b)
print('a / b\t:', a / b)  # gives float answer
print('a // b\t:', a // b)  # performs floor division
print('a % b\t:', a % b)
print('a ** b\t:', a ** b)  # exponential
print('round(a / b)\t:', round(a / b))
print('round(a / b, 2)\t:', round(a / b, 2))

